package ifstatements;

public class Local_Variables
{
    static void letter()
    {
    	char letter = 'M';
    	System.out.println(letter);
    }
    static void name()
    {
    	String name="Mary";
    	System.out.println(name);
    }
	public static void main(String[] args) 
	{
	 letter();
	 name();
	}
}
