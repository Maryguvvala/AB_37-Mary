package ifstatements;

public class Ifelseifstatement1 {

	public static void main(String[] args) 
	{
		int a=18;
		if (a>18)
		{
			System.out.println("c1");
		}
		else if (a<=16)
		{
			System.out.println("c2");
		}
		else if (a!=18)
		{
			System.out.println("c3");
		}
		else if (a>20)
		{
			System.out.println("c4");
		}
		else
		{
			System.out.println("c5");
		}
	}

}
