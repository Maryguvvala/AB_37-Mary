package ifstatements;

public class Ifelseifstatement {

	public static void main(String[] args)
	{
      int a=5;
      int b=6;
      int sum=a+b;
       if (sum<=11)
       {
    	   System.out.println("c1");
       }
       else if (sum==11)
       {
    	   System.out.println("c2");
       }
       else if (sum>=11)
       {
    	   System.out.println("c3");
       }
       else
       {
    	   System.out.println("false condition");
       }
       
	}
	

}
