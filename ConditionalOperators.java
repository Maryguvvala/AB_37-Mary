package ifstatements;

public class ConditionalOperators //Only true statements will get executed
{
	public static void main(String[] args) {// 6 if block statements
		int a;
		int b;
		a=5;
		b=6;
		if (a>b)
		{
			System.out.println("1");
		}
		if (a<b)
		{
			System.out.println("2");
		}
		if (a>=b)
		{
			System.out.println("3");
		}
		if (a<=b)
		{
			System.out.println("4");
		}
		if (a==b)
		{
			System.out.println("5");
		}
		if (a!=b)
		{
			System.out.println("6");
		}
	}
}
